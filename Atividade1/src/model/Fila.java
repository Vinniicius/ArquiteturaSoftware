package model;

public class Fila {
	private int idFila;
	private String numFila;
	
	public int getIdFila() {
		return idFila;
	}
	public void setIdFila(int idFila) {
		this.idFila = idFila;
	}
	public String getNumFila() {
		return numFila;
	}
	public void setNumFila(String numFila) {
		this.numFila = numFila;
	}
}
